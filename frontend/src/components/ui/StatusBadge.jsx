const statusMap = {
    Scheduled: "bg-blue-100 text-blue-700",
    Completed: "bg-green-100 text-green-700",
    Cancelled: "bg-red-100 text-red-700",
};

export const StatusBadge = ({ status }) => {
    const styles = statusMap[status] || "bg-gray-100 text-gray-700";
    return (
        <span className={`px-2 py-1 rounded-full text-sm font-medium ${styles}`}>
            {status}
        </span>
    );
}